package practice_problem_02;

import java.util.Scanner;

public class print_series
{
    public static void main(String[] args)
    {
        for (int i = 2; i <= 20; i += 2)
        {
            System.out.print(i + " ");
        }
        for (int i = 1; i <= 20; i += 2)
        {
            System.out.print(i + " ");
        }
    }
}

